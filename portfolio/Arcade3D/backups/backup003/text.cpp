void arc::BMPSTRING::putPixel(int x, int y,textColor * color)
{
	// Jesli wykraczamy poza ekran - nie rysujemy
	if( (x >= 0) && (x < w) && (y >= 0) && (y < h) )
	{
	    /* Zamieniamy poszczególne sk³adowe koloru na format koloru pixela */
	    Uint32 pixel = SDL_MapRGB(srf->format, color->R, color->G, color->B);
	 
	    /* Pobieramy informacji ile bajtów zajmuje jeden pixel */
	    int bpp = srf->format->BytesPerPixel;
	 
	    /* Obliczamy adres pixela */
	    volatile Uint8 *p = (Uint8 *)srf->pixels + y * srf->pitch + x * bpp;
	 
	    /* Ustawiamy wartoœæ pixela, w zale¿noœci od formatu powierzchni*/
	    switch(bpp)
	    {
	        case 1: //8-bit
	            *p = pixel;
	            break;
	 
	        case 2: //16-bit
	            *(Uint16 *)p = pixel;
	            break;
	 
	        case 3: //24-bit
	            if(SDL_BYTEORDER == SDL_BIG_ENDIAN) {
	                p[0] = (pixel >> 16) & 0xff;
	                p[1] = (pixel >> 8) & 0xff;
	                p[2] = pixel & 0xff;
	            } else {
	                p[0] = pixel & 0xff;
	                p[1] = (pixel >> 8) & 0xff;
	                p[2] = (pixel >> 16) & 0xff;
	            }
	            break;
	 
	        case 4: //32-bit
	            *(Uint32 *)p = pixel;
	            break;
	    }
	}
}
void arc::BMPSTRING::InsertString(int x,int y, std::string text)
{
int size = text.size();

	for(int i = 0 ; i < size ; i++)
	{
	PutChar(x+(i*8),y,text[i]);
	}	
}

void arc::BMPSTRING::InsertString(int x,int y, int value)
{
std::string text;
std::stringstream istr;

	istr << value;
	istr >> text;

int size = text.size();

	for(int i = 0 ; i < size ; i++)
	{
	PutChar(x+(i*8),y,text[i]);
	}	
}


